
public class Day32ade {

    public static void main(String[] args) {
        for (int i = 1; i <= 20; i++) {
            System.out.println("Iterasi ke-" + i);
        }

        int angka = 1;
        while (angka <= 20) {
            System.out.println("Angka: " + angka);
            angka++;
        }
    }
}
